<template>
  <div class="audio-player-container" :class="{ 'dark-theme': darkMode }">
    <div ref="aplayerContainer" class="aplayer-container"></div>
  </div>
</template>

<script setup>
import { ref, onMounted, onBeforeUnmount, watch, nextTick } from "vue";
import APlayer from "aplayer";
import "aplayer/dist/APlayer.min.css";

// Props ÂÆö‰πâ
const props = defineProps({
  // Èü≥È¢ëÊñá‰ª∂ÂàóË°®
  audioList: {
    type: Array,
    default: () => [],
  },
  // ÂΩìÂâçÊí≠ÊîæÁöÑÈü≥È¢ë‰ø°ÊÅØ
  currentAudio: {
    type: Object,
    default: () => null,
  },
  // ÊòØÂê¶‰∏∫Ê∑±Ëâ≤Ê®°Âºè
  darkMode: {
    type: Boolean,
    default: false,
  },
  // ÊòØÂê¶Ëá™Âä®Êí≠Êîæ
  autoplay: {
    type: Boolean,
    default: false,
  },
  // ÊòØÂê¶ÊòæÁ§∫Êí≠ÊîæÂàóË°®
  showPlaylist: {
    type: Boolean,
    default: true,
  },
  // Êí≠ÊîæÂô®‰∏ªÈ¢òËâ≤
  theme: {
    type: String,
    default: "#3b82f6",
  },
  // Êí≠ÊîæÂô®Ê®°ÂºèÔºö'normal', 'mini', 'fixed'
  mode: {
    type: String,
    default: "normal",
  },
  // ÊòØÂê¶Âæ™ÁéØÊí≠Êîæ
  loop: {
    type: String,
    default: "all", // 'all', 'one', 'none'
  },
  // Êí≠ÊîæÈ°∫Â∫è
  order: {
    type: String,
    default: "list", // 'list', 'random'
  },
  // Êí≠ÊîæÂàóË°®ÊòØÂê¶ÊäòÂè†
  listFolded: {
    type: Boolean,
    default: true,
  },
  // Êí≠ÊîæÂàóË°®ÊúÄÂ§ßÈ´òÂ∫¶
  listMaxHeight: {
    type: String,
    default: "250px",
  },
  // Èü≥Èáè
  volume: {
    type: Number,
    default: 0.7,
  },
  // ÊòØÂê¶ÊòæÁ§∫Ê≠åËØç
  showLrc: {
    type: Boolean,
    default: false,
  },
});

// Emits ÂÆö‰πâ
const emit = defineEmits(["play", "pause", "ended", "timeupdate", "loadstart", "canplay", "error", "listswitch", "listadd", "listremove", "listclear", "noticeshow", "noticehide"]);

// ÂìçÂ∫îÂºèÊï∞ÊçÆ
const aplayerContainer = ref(null);
const aplayerInstance = ref(null);

// ËÆ°ÁÆó‰∏ªÈ¢òËâ≤
const getThemeColor = () => {
  if (props.darkMode) {
    return "#60a5fa"; // Ê∑±Ëâ≤Ê®°Âºè‰∏ã‰ΩøÁî®ËæÉ‰∫ÆÁöÑËìùËâ≤
  }
  return props.theme;
};

// ÂàùÂßãÂåñ APlayer
const initAPlayer = () => {
  if (!aplayerContainer.value) return;

  // ÈîÄÊØÅÁé∞ÊúâÂÆû‰æã
  if (aplayerInstance.value) {
    aplayerInstance.value.destroy();
    aplayerInstance.value = null;
  }

  // ÂáÜÂ§áÈü≥È¢ëÊï∞ÊçÆ
  const audioData = prepareAudioData();
  if (audioData.length === 0) return;

  // APlayer ÈÖçÁΩÆ
  const options = {
    container: aplayerContainer.value,
    audio: audioData,
    autoplay: props.autoplay,
    theme: getThemeColor(),
    loop: props.loop,
    order: props.order,
    preload: "metadata",
    volume: props.volume,
    mutex: true,
    listFolded: props.listFolded,
    listMaxHeight: props.listMaxHeight,
    lrcType: props.showLrc ? 3 : 0,
    storageName: "cloudpaste-aplayer",
  };

  // Ê†πÊçÆÊ®°ÂºèË∞ÉÊï¥ÈÖçÁΩÆ
  if (props.mode === "mini") {
    options.mini = true;
  } else if (props.mode === "fixed") {
    options.fixed = {
      type: "fixed",
      position: "bottom",
    };
  }

  try {
    // ÂàõÂª∫ APlayer ÂÆû‰æã
    aplayerInstance.value = new APlayer(options);

    // ÁªëÂÆö‰∫ã‰ª∂ÁõëÂê¨Âô®
    bindEvents();

    // Â∫îÁî®‰∏ªÈ¢òÊ†∑Âºè
    applyThemeStyles();
  } catch (error) {
    console.error("APlayer ÂàùÂßãÂåñÂ§±Ë¥•:", error);
    emit("error", error);
  }
};

// ÂáÜÂ§áÈü≥È¢ëÊï∞ÊçÆ
const prepareAudioData = () => {
  const audioData = [];

  // Â¶ÇÊûúÊúâÂΩìÂâçÈü≥È¢ëÔºå‰ºòÂÖàÊ∑ªÂä†
  if (props.currentAudio) {
    audioData.push(formatAudioItem(props.currentAudio));
  }

  // Ê∑ªÂä†Êí≠ÊîæÂàóË°®‰∏≠ÁöÑÂÖ∂‰ªñÈü≥È¢ë
  if (props.audioList && props.audioList.length > 0) {
    props.audioList.forEach((audio) => {
      // ÈÅøÂÖçÈáçÂ§çÊ∑ªÂä†ÂΩìÂâçÈü≥È¢ë
      if (!props.currentAudio || audio.url !== props.currentAudio.url) {
        audioData.push(formatAudioItem(audio));
      }
    });
  }

  return audioData;
};

// Ê†ºÂºèÂåñÈü≥È¢ëÈ°π
const formatAudioItem = (audio) => {
  return {
    name: audio.name || audio.title || "Êú™Áü•Èü≥È¢ë",
    artist: audio.artist || "Êú™Áü•Ëâ∫ÊúØÂÆ∂",
    url: audio.url,
    cover: audio.cover || audio.poster || generateDefaultCover(audio.name),
    lrc: audio.lrc || audio.lyrics,
    theme: getThemeColor(),
  };
};

// ÁîüÊàêÈªòËÆ§Â∞ÅÈù¢
const generateDefaultCover = (name) => {
  // ‰ΩøÁî®Êñá‰ª∂ÂêçÈ¶ñÂ≠óÊØçÁîüÊàêÁÆÄÂçïÁöÑÈªòËÆ§Â∞ÅÈù¢
  const firstChar = (name || "M")[0].toUpperCase();
  const canvas = document.createElement("canvas");
  canvas.width = 100;
  canvas.height = 100;
  const ctx = canvas.getContext("2d");

  // ËÉåÊôØËâ≤
  ctx.fillStyle = getThemeColor();
  ctx.fillRect(0, 0, 100, 100);

  // ÊñáÂ≠ó
  ctx.fillStyle = "#ffffff";
  ctx.font = "bold 40px Arial";
  ctx.textAlign = "center";
  ctx.textBaseline = "middle";
  ctx.fillText(firstChar, 50, 50);

  return canvas.toDataURL();
};

// ÁªëÂÆö‰∫ã‰ª∂ÁõëÂê¨Âô®
const bindEvents = () => {
  if (!aplayerInstance.value) return;

  const ap = aplayerInstance.value;

  ap.on("play", () => {
    emit("play", {
      audio: ap.list.audios[ap.list.index],
      index: ap.list.index,
    });
  });

  ap.on("pause", () => {
    emit("pause", {
      audio: ap.list.audios[ap.list.index],
      index: ap.list.index,
    });
  });

  ap.on("ended", () => {
    emit("ended", {
      audio: ap.list.audios[ap.list.index],
      index: ap.list.index,
    });
  });

  ap.on("timeupdate", () => {
    emit("timeupdate", {
      currentTime: ap.audio.currentTime,
      duration: ap.audio.duration,
      percentage: (ap.audio.currentTime / ap.audio.duration) * 100,
    });
  });

  ap.on("loadstart", () => {
    emit("loadstart");
  });

  ap.on("canplay", () => {
    emit("canplay");
  });

  ap.on("error", (error) => {
    // Ê£ÄÊü•ÊòØÂê¶ÊòØService WorkerÁõ∏ÂÖ≥ÁöÑËØØÊä•ÈîôËØØ
    if (error?.target?.src?.includes(window.location.origin) && ap?.list?.audios?.[ap.list.index]?.url?.startsWith("https://")) {
      console.log("üéµ ÂøΩÁï•Service WorkerÁõ∏ÂÖ≥ÁöÑËØØÊä•ÈîôËØØÔºåÈü≥È¢ëÂÆûÈôÖÂèØ‰ª•Ê≠£Â∏∏Êí≠Êîæ");
      return; // ÂøΩÁï•Service WorkerËØØÊä•ÈîôËØØ
    }

    console.error("APlayer Êí≠ÊîæÈîôËØØ:", error);
    emit("error", error);
  });

  ap.on("listswitch", (index) => {
    // Á°Æ‰øùÁ¥¢ÂºïÊúâÊïà‰∏îÈü≥È¢ëÊï∞ÊçÆÂ≠òÂú®
    const audio = ap.list && ap.list.audios && ap.list.audios[index] ? ap.list.audios[index] : null;

    emit("listswitch", {
      audio: audio,
      index: index,
    });
  });
};

// Â∫îÁî®‰∏ªÈ¢òÊ†∑Âºè
const applyThemeStyles = () => {
  if (!aplayerContainer.value) return;

  nextTick(() => {
    const aplayerElement = aplayerContainer.value.querySelector(".aplayer");
    if (!aplayerElement) return;

    // Êõ¥Êñ∞‰∏ªÈ¢òËâ≤
    const themeColor = getThemeColor();
    aplayerElement.style.setProperty("--aplayer-theme", themeColor);

    // Â∫îÁî®ÊöóËâ≤‰∏ªÈ¢òÁ±ª
    if (props.darkMode) {
      aplayerContainer.value.classList.add("dark-theme");
    } else {
      aplayerContainer.value.classList.remove("dark-theme");
    }
  });
};

// ÂÖ¨ÂºÄÁöÑÊñπÊ≥ï
const play = () => {
  if (aplayerInstance.value) {
    aplayerInstance.value.play();
  }
};

const pause = () => {
  if (aplayerInstance.value) {
    aplayerInstance.value.pause();
  }
};

const toggle = () => {
  if (aplayerInstance.value) {
    aplayerInstance.value.toggle();
  }
};

const seek = (time) => {
  if (aplayerInstance.value) {
    aplayerInstance.value.seek(time);
  }
};

const setVolume = (volume) => {
  if (aplayerInstance.value) {
    aplayerInstance.value.volume(volume);
  }
};

const switchAudio = (index) => {
  if (aplayerInstance.value && aplayerInstance.value.list) {
    aplayerInstance.value.list.switch(index);
  }
};

const addAudio = (audio) => {
  if (aplayerInstance.value && aplayerInstance.value.list) {
    aplayerInstance.value.list.add(formatAudioItem(audio));
  }
};

const removeAudio = (index) => {
  if (aplayerInstance.value && aplayerInstance.value.list) {
    aplayerInstance.value.list.remove(index);
  }
};

// Êö¥Èú≤ÊñπÊ≥ïÁªôÁà∂ÁªÑ‰ª∂
defineExpose({
  play,
  pause,
  toggle,
  seek,
  setVolume,
  switchAudio,
  addAudio,
  removeAudio,
  getInstance: () => aplayerInstance.value,
});

// ÁõëÂê¨Â±ûÊÄßÂèòÂåñ
watch(
  () => props.darkMode,
  () => {
    applyThemeStyles();
  },
  { immediate: false }
);

watch(
  () => props.theme,
  () => {
    if (aplayerInstance.value) {
      aplayerInstance.value.theme(getThemeColor());
    }
    applyThemeStyles();
  }
);

watch(
  () => [props.currentAudio, props.audioList, props.loop, props.order],
  () => {
    initAPlayer();
  },
  { deep: true }
);

watch(
  () => props.volume,
  (newVolume) => {
    setVolume(newVolume);
  }
);

// ÁîüÂëΩÂë®Êúü
onMounted(() => {
  nextTick(() => {
    initAPlayer();
  });
});

onBeforeUnmount(() => {
  if (aplayerInstance.value) {
    aplayerInstance.value.destroy();
    aplayerInstance.value = null;
  }
});
</script>

